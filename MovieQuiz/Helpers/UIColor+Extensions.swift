import Foundation
import UIKit

extension UIColor {
    
    public static var customWhite: UIColor {
        UIColor(named: "YP White") ?? UIColor.black
    }
    
    public static var customRed: UIColor {
        UIColor(named: "YP Red") ?? UIColor.black
    }
    
    public static var customGreen: UIColor {
        UIColor(named: "YP Green") ?? UIColor.black
    }
}
